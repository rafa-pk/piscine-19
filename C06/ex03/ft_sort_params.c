#include <unistd.h>

int	main(int ac, char *av[])
{
	int	i;

	i = 1;
	while (i < ac - 1)
	{
		j = 1;
		while (j < ac - 1)
		{
			if (ft_strcmp(av[j], av[j + 1]) > 0)
				ft_swap(&av[i], &av[i + 1]);
			j++;
		}
		i++;
	}
	write_params(ac, av);
}
